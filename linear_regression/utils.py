import numpy as np

C = TypeVar("C", bound=Callable)


def get_ast_Call_name(node: ast.Call):
    try:
        name = node.func.id
    except AttributeError:
        name = node.func.attr
    return name


def num_loops_of_callable(func: C) -> int:
    loop_nodes = (
        ast.For,
        ast.While,
        ast.AsyncFor,
        ast.ListComp,
        ast.DictComp,
        ast.SetComp,
        ast.GeneratorExp,
    )
    parsed = ast.parse(inspect.getsource(func))
    return sum(isinstance(node, loop_nodes) for node in ast.walk(parsed))


def max_allowed_loops(max_loops: int) -> Callable[[C], C]:
    if max_loops < 0:
        raise ValueError(f"max_loops ({max_loops}) cannot be negative")

    def decorator(func: C) -> C:
        try:
            n_loops = num_loops_of_callable(func)
        except SyntaxError:
            n_loops = -1
            print(f"cannot parse {func.__name__}")

        correct_number_of_loops = n_loops <= max_loops

        msg = (
            f"{func.__name__} uses too many loops: allowed: {max_loops},"
            f" used: {n_loops}"
        )
        if not correct_number_of_loops:
            print(msg)

        def assert_not_too_many_loops():
            assert correct_number_of_loops, msg

        func.assert_not_too_many_loops = assert_not_too_many_loops
        return func

    return decorator


